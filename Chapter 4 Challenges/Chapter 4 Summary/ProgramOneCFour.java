public class ProgramOneCFour {
   
   public static void main(String[] args) {
      
      // Output
      System.out.print("The odd numbers from 0 to 100 are: ");
      
      // For loop
      //   initialize  test    update
      for (int i = 1; i < 100; i += 2) {
         System.out.print(i + " ");
      }
      
   } // Main method
   
} // End class